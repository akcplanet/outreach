package org.outreach.cts.dto;

public class ReportDTO {
	
	public String Council;
	public String School;
	public String Activity;
	public String Type;
	public String POC1;
	public String POC2;
	public String poc3;
	public String Date;
	public String Impacted;
	public String Hours;
	
	
	
	public String getCouncil() {
		return Council;
	}
	public void setCouncil(String council) {
		Council = council;
	}
	public String getSchool() {
		return School;
	}
	public void setSchool(String school) {
		School = school;
	}
	public String getActivity() {
		return Activity;
	}
	public void setActivity(String activity) {
		Activity = activity;
	}
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}
	public String getPOC1() {
		return POC1;
	}
	public void setPOC1(String pOC1) {
		POC1 = pOC1;
	}
	public String getPOC2() {
		return POC2;
	}
	public void setPOC2(String pOC2) {
		POC2 = pOC2;
	}
	public String getPoc3() {
		return poc3;
	}
	public void setPoc3(String poc3) {
		this.poc3 = poc3;
	}
	public String getDate() {
		return Date;
	}
	public void setDate(String date) {
		Date = date;
	}
	public String getImpacted() {
		return Impacted;
	}
	public void setImpacted(String impacted) {
		Impacted = impacted;
	}
	public String getHours() {
		return Hours;
	}
	public void setHours(String hours) {
		Hours = hours;
	}
	
	
	
	

}
